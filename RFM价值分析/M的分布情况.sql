SELECT
	CASE
		WHEN M > 10000 THEN '10000以上'
		WHEN M BETWEEN 5000 AND 10000 THEN '5000 - 10000'
		WHEN M BETWEEN 1000 AND 4999 THEN '1000 - 5000'
		WHEN M BETWEEN 500 AND 999 THEN '500 - 1000'
		WHEN M <= 500 THEN '500以下'
	END AS 价格区间,
	COUNT(uid) num
FROM user_rfm
GROUP BY CASE
		WHEN M >10000
		THEN '10000以上'
		WHEN M BETWEEN 5000 AND 10000
		THEN '5000 - 10000'
		WHEN M BETWEEN 1000 AND 4999
		THEN '1000 - 5000'
		WHEN M BETWEEN 500 AND 999
		THEN '500 - 1000'
		WHEN M <= 500
		THEN '500以下'
	END;
